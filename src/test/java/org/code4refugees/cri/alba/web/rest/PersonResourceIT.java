    @Test
    @Transactional
    void putNewPerson() throws Exception {
        // Initialize the database
        personRepository.saveAndFlush(person);

        int databaseSizeBeforeUpdate = personRepository.findAll().size();

        // Update the person
        Person updatedPerson = personRepository.findById(person.getId()).get();
        // Disconnect from session so that the updates on updatedPerson are not directly saved in db
        em.detach(updatedPerson);
        updatedPerson
            .personalIdentifier(UPDATED_PERSONAL_IDENTIFIER)
            .identifierType(UPDATED_IDENTIFIER_TYPE)
            .firstName(UPDATED_FIRST_NAME)
            .lastName(UPDATED_LAST_NAME)
            .email(UPDATED_EMAIL)
            .phoneNumber(UPDATED_PHONE_NUMBER)
            .firstSpokenLanguage(UPDATED_FIRST_SPOKEN_LANGUAGE)
            .secondSpokenLanguage(UPDATED_SECOND_SPOKEN_LANGUAGE);

        restPersonMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedPerson.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedPerson))
            )
            .andExpect(status().isOk());

        // Validate the Person in the database
        List<Person> personList = personRepository.findAll();
        assertThat(personList).hasSize(databaseSizeBeforeUpdate);
        Person testPerson = personList.get(personList.size() - 1);
        assertThat(testPerson.getPersonalIdentifier()).isEqualTo(UPDATED_PERSONAL_IDENTIFIER);
        assertThat(testPerson.getIdentifierType()).isEqualTo(UPDATED_IDENTIFIER_TYPE);
        assertThat(testPerson.getFirstName()).isEqualTo(UPDATED_FIRST_NAME);
        assertThat(testPerson.getLastName()).isEqualTo(UPDATED_LAST_NAME);
        assertThat(testPerson.getEmail()).isEqualTo(UPDATED_EMAIL);
        assertThat(testPerson.getPhoneNumber()).isEqualTo(UPDATED_PHONE_NUMBER);
        assertThat(testPerson.getFirstSpokenLanguage()).isEqualTo(UPDATED_FIRST_SPOKEN_LANGUAGE);
        assertThat(testPerson.getSecondSpokenLanguage()).isEqualTo(UPDATED_SECOND_SPOKEN_LANGUAGE);
    }

